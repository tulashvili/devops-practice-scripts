# Переменные для результатов
found=()
missing=()

# Проверка каждого IP
for ip in "${ips[@]}"; do
  line=$(grep "^$ip" /etc/hosts)
  if [[ -n "$line" ]]; then
    fqdn=$(echo "$line" | awk '{print $2}')
    found+=("$ip -> $fqdn")
  else
    missing+=("$ip")
  fi
done

# Вывод результатов
echo "Найденные IP-адреса и их FQDN в /etc/hosts:"
printf '%s\n' "${found[@]}"

echo -e "\nОтсутствующие IP-адреса в /etc/hosts:"
printf '%s\n' "${missing[@]}"

